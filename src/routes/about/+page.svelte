<Headline headline="Hi, I'm Julien&nbsp;Pradet," subtitle="do you want to learn more about me?" />

<p>
	I'm a french web developer that has been enjoying programming for over 10 years. But I've been
	exploring generative art only since August 2021.
</p>

<p>
	It might just be my way to go back in time. In 2010, I had to choose what to do for a living. Back
	then I was creating some small illustrations, abstract digital painting, sculptures on Blender and
	sharing those with other hobbyists on forums. It was a tough choice between becoming a Designer or
	a Developer. But in the end I didn't want to make it my main occupation, so I happily became a
	developer.
</p>

<p>
	Along the way I completly stopped creating images. I was creating code instead. You can even find a bit of my developer journey on <a href="https://www.julienpradet.fr/">my french blog</a>. But something
	changed in April 2021 when I joined my latest job at an online art gallery. Seeing some art
	everyday helped me find some inspiration. The urge to create things and express myself through
	images came back.
</p>

<p>
	Since then, I had fun exploring the field of generative art, learning new technologies and
	creating stuff. I've also joined the Birb's nest, <a href="https://twitter.com/sableRaph"
		>@sableraph</a
	>'s community that motivated me exploring new things. And a year later, in August 2022 I was
	publishing <a href="https://www.fxhash.xyz/generative/18713">my first collection</a> on
	<a href="https://www.fxhash.xyz/u/Julien%20Pradet">fxhash</a>.
</p>

<p>
	I never envisionned creating art as a lonely experience and this is why I'm creating this website:
	to share my process, my creations and hopefully learn about you along the way.
</p>

